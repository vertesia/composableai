#!/usr/bin/env node
/**
 * Overlay the opinionated content-app preset onto the minimal plugin template.
 *
 * This is intentionally separate from `--full`: the full scaffold copies generic
 * examples, while this preset installs real app-owned content types,
 * interactions, a process, UI routes, and setup/exercise scripts.
 */
import { cpSync, existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const preset = join(root, 'content-app');

function readPackageName() {
    const packageJson = JSON.parse(readFileSync(join(root, 'package.json'), 'utf8'));
    if (!packageJson.name || typeof packageJson.name !== 'string') {
        throw new Error('package.json is missing a string name');
    }
    return packageJson.name;
}

function copy(from, to) {
    const source = join(preset, from);
    if (!existsSync(source)) {
        throw new Error(`content-app preset path missing: ${from}`);
    }
    cpSync(source, join(root, to), { recursive: true });
    console.log(`  ${from} -> ${to}`);
}

function writeGeneratedAppName(packageName) {
    const targetDir = join(root, 'src', 'tool-server');
    mkdirSync(targetDir, { recursive: true });
    writeFileSync(
        join(targetDir, 'app-name.ts'),
        [
            '// Generated by scripts/apply-content-app.mjs.',
            '// Keep this equal to package.json name and VITE_APP_NAME.',
            `export const APP_NAME = ${JSON.stringify(packageName)};`,
            '',
        ].join('\n'),
    );
}

function patchPackageScripts() {
    const packageJsonPath = join(root, 'package.json');
    const packageJson = JSON.parse(readFileSync(packageJsonPath, 'utf8'));
    packageJson.scripts = {
        ...packageJson.scripts,
        'seed:content': 'node scripts/seed-content-app.mjs',
        'exercise:content': 'node scripts/exercise-content-app.mjs',
    };
    writeFileSync(packageJsonPath, `${JSON.stringify(packageJson, null, 4)}\n`);
}

if (!existsSync(preset)) {
    console.error('apply-content-app: content-app preset directory not found.');
    process.exit(1);
}

const packageName = readPackageName();

console.log('Applying content app scaffold into src/...');
copy('src/tool-server', 'src/tool-server');
copy('src/ui/app', 'src/ui/app');
copy('scripts', 'scripts');
copy('docs', 'docs');
writeGeneratedAppName(packageName);
patchPackageScripts();
console.log('Done. Content app scaffold applied.');
